// Câu lệnh rẽ nhánh - Switch

var date = 2;

switch (date) {
  case 2:
    console.log("thu 2");
    break;
  case 3:
    console.log("thu 3");
    break;
  case 4:
    console.log("thu 4");
    break;
  case 5:
    console.log("thu 5");
    break;
  case 6:
    console.log("thu 6");
    break;
  default:
    console.log("khong biet thu may ca!!");
}
