// Câu lệnh rẽ nhánh if-else

var date = 10;

// dùng else-if thì đúng 1 điều kiện sẽ break luôn, còn nếu dùng if bình thường thì sẽ cứ chạy cho đến hết chương trình
if (date === 2) {
  console.log("Hôm nay là thứ 2");
} else if (date === 3) {
  console.log("Hôm nay là thứ 3");
} else if (date === 4) {
  console.log("Hôm nay là thứ 4");
} else {
  console.log("i dont know");
}
