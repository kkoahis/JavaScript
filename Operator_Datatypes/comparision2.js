// Toán tử so sánh phần 2
/*
   === và !==
*/
var a = 1;
var b = '1';
var b2 = 1;

console.log(a == b2); // Chỉ quan tâm đến value của 2 biển mà thôi
console.log(a === b); // So sánh cả value và datatype

console.log(a != b2); // Chỉ quan tâm đến value của 2 biển mà thôi
console.log(a !== b); // So sánh cả value và datatype
